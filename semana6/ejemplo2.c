/*Se da un ejemplo del uso de arreglos mediante multiplicaci√≥n*/

#include<stdio.h>

int main()
{
	int i, n=10;
	float numero[n];
	
	for (i=0;i<n;i++) {
		scanf("&f \n",&numeros[n]);

		numeros[n]*=2; // Multiplica por 2 el entero dado por el usuario.
		printf("%f \n", numeros[n]); // Imprime en la pantalla el valor del arreglo multiplicado por 2.
	}
	return 0;
}
